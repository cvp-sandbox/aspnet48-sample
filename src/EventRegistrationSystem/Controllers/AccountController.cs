        //
        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            try
            {
                // Call the API to register the user
                var registerRequest = new
                {
                    Email = model.Email,
                    Password = model.Password,
                    ConfirmPassword = model.ConfirmPassword
                };

                // For registration, we don't have a user context yet, so pass empty values
                var response = await _apiClient.PostAsync<object, RegisterResponse>(
                    "api/users/register", 
                    registerRequest, 
                    string.Empty, 
                    new string[] { });

                if (response.Success)
                {
                    // Sign in the user using ASP.NET Identity
                    var user = await UserManager.FindByEmailAsync(model.Email);
                    if (user == null)
                    {
                        // If the user doesn't exist in the local database yet, create it
                        user = new ApplicationUser { UserName = model.Email, Email = model.Email };
                        await UserManager.CreateAsync(user);
                    }
                    
                    await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);
                    
                    return RedirectToAction("Index", "Home");
                }
                else
                {
                    foreach (var error in response.Errors)
                    {
                        ModelState.AddModelError("", error);
                    }
                    return View(model);
                }
            }
            catch
            {
                // Fallback to the original implementation if API call fails
                var user = new ApplicationUser { UserName = model.Email, Email = model.Email };
                var result = await UserManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);

                    // Check if this is the first user
                    var roleRepository = DependencyResolver.Current.GetService<IRoleRepository>();
                    bool isFirstUser = await IsFirstUserAsync();

                    // Add user to default role
                    roleRepository.AddUserToRole(user.Id, Authorization.Roles.User);

                    // If first user, make them an admin
                    if (isFirstUser)
                    {
                        roleRepository.AddUserToRole(user.Id, Authorization.Roles.Admin);
                    }

                    return RedirectToAction("Index", "Home");
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

        private static async Task<bool> IsFirstUserAsync()
        {
            var userStore = new SqliteUserStore();
            bool isFirstUser = await userStore.GetUserCountAsync() == 1;
            return isFirstUser;
        }

